const btn = document.getElementById('langToggleBtn');
const allLinks = document.querySelectorAll('[data-i18n]');

// تحميل اللغة الحالية من localStorage أو تعيين الافتراضية
let currentLang = localStorage.getItem('lang') || 'en';
setLanguage(currentLang); // تطبيق اللغة المحفوظة

// عند الضغط على زر التبديل
btn.addEventListener('click', () => {
  currentLang = currentLang === 'en' ? 'ar' : 'en';
  localStorage.setItem('lang', currentLang); // حفظها في localStorage
  setLanguage(currentLang); // تطبيق اللغة الجديدة
});

function setLanguage(lang) {
  const dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('dir', dir);
  btn.innerText = lang === 'ar' ? 'English' : 'عربي';

  // تحديث الترجمة
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.innerText = translations[lang][key];
    }
  });

  // تحديث المسافات للغة
  document.querySelectorAll('[data-i18n]').forEach(link => {
    link.className = link.className.replace(/\bme-(\d+)\b/g, 'tmp-$1');
    link.className = link.className.replace(/\bms-(\d+)\b/g, 'me-$1');
    link.className = link.className.replace(/\btmp-(\d+)\b/g, 'ms-$1');
  });

  // تحديث أيقونة الأسهم (مثال 1)
  document.querySelectorAll('.read-more-link .arrow-icon').forEach(icon => {
    icon.classList.remove('fa-arrow-right', 'fa-arrow-left');
    icon.classList.add(lang === 'ar' ? 'fa-arrow-left' : 'fa-arrow-right');
    icon.classList.toggle('ps-3', lang === 'en');
    icon.classList.toggle('pe-3', lang === 'ar');
  });

  // تحديث أيقونات chevron (مثال 2)
  document.querySelectorAll('.service-btn .arrow-icon').forEach(icon => {
    icon.classList.remove('fa-chevron-left', 'fa-chevron-right');
    icon.classList.add(lang === 'ar' ? 'fa-chevron-left' : 'fa-chevron-right');
    icon.classList.toggle('ms-1', lang === 'en');
    icon.classList.toggle('me-1', lang === 'ar');
  });
}

